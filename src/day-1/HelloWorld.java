// by the way, this is a comment (preceded by two forward slashes). The Java
// compiler will ignore these, so you can use comments to write notes about what
// the code does or for anything else you might find useful

// this is the class declaration. You don't need to worry about it for now, but
// just know the name of the class (right after the word class), must be the
// same as the name of your file. Example: the name of this class is Main, and
// the name of the file is HellWorld.java
public class HelloWorld {

  // this is the main function. Anything that goes in between the two curly
  // brackets here is run, so this is where you'll want to put all your code
  // there (at least for now)
  public static void main(String[] args) {

    // this statement prints out the string "Hello World"
    // you can use System.out.println() at any time to print something to the
    // console what you want to print goes inside the brackets
    System.out.println("Hello world!");
  }
}
