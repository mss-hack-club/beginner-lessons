public class DataTypes {

  public static void main(String[] args) {
    int myNum = 5; // declare integer
    float floatNum = 5.99f;// declare float
    char myCharacter = '%';// declare character
    boolean myBool = true; // declare boolean
    String myText = "Hello";// declare string

    System.out.println(myNum);// output integer variable
    System.out.println(floatNum);// output float variable
    System.out.println(myBool);// output boolean variable
    System.out.println(myCharacter);// output char variable
    System.out.println(myText);// output string variable
    System.out.println(myNum + " " + myText);// outpitn integer and string variable
  }
}
